package at.htlpinkafeld.database_manager.pojo;

/**
 *
 * @author Martin Six
 */
public class Department {

    private int deptno;
    private String dname;
    private String loc;

    public Department(int deptno, String dname, String loc) {
        this.deptno = deptno;
        this.dname = dname;
        this.loc = loc;
    }

    public Department(String dname, String loc) {
        deptno = -1;
        this.dname = dname;
        this.loc = loc;
    }

    public int getDeptno() {
        return deptno;
    }

    public void setDeptno(int deptno) {
        this.deptno = deptno;
    }

    public String getDname() {
        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    public String getLoc() {
        return loc;
    }

    public void setLoc(String loc) {
        this.loc = loc;
    }

    @Override
    public String toString() {
        return deptno + "  " + dname;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 29 * hash + this.deptno;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Department other = (Department) obj;
        if (this.deptno != other.deptno) {
            return false;
        }
        return true;
    }

}
